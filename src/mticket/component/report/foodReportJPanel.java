     public void initData(List<Object[]> objs) {
        Food f = new foodDAO().selectById(String.valueOf(objs.get(0)[0]));

        jLabel2.setText(f.getTenThucAn());
        txtMoTa.setText(f.getMoTaThucAn());
        jLabel3.setText("Doang Thu:  " + mticketUtils.toCurrency(Double.parseDouble(String.valueOf(objs.get(0)[1]))) + " (" + objs.get(0)[2] + "%)"); //mticketUtils.toCurrency(mC.getValues()[0])
        mticketUtils.setIcon(new ImageIcon(getClass().getResource("/mticket/image/" + f.getHinhThucAn())), pictureBox1, 16);

        jLabel9.setText("Doanh Thu");
        mticketUtils.setIcon(new ImageIcon(), pictureBox2, 16);

        jLabel13.setText("Doanh Thu");
        mticketUtils.setIcon(new ImageIcon(), pictureBox3, 16);

        if (objs.size() > 1) {
           Food f_2 = new foodDAO().selectById(String.valueOf(objs.get(1)[0]));
            jLabel9.setText(mticketUtils.toCurrency(Double.parseDouble(String.valueOf(objs.get(1)[1]))) + " (" + objs.get(1)[2] + "%)");
            mticketUtils.setIcon(new ImageIcon(getClass().getResource("/mticket/image/" + f_2.getHinhThucAn())), pictureBox2, 16);
            if (objs.size() == 3) {
               Food f_3 = new foodDAO().selectById(String.valueOf(objs.get(2)[0]));
                jLabel13.setText(mticketUtils.toCurrency(Double.parseDouble(String.valueOf(objs.get(2)[1]))) + " (" + objs.get(2)[2] + "%)");
                mticketUtils.setIcon(new ImageIcon(getClass().getResource("/mticket/image/" + f_3.getHinhThucAn())), pictureBox3, 16);
            }
        }
    }

